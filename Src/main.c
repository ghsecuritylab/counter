#include "trace.h"



#include "flash_if.h"
#include "image.h"

CONFIG  xConfig;

int main(void)
{
    BILL_COUNTER*   pBC;

    SYS_init();

    TRACE_setEnable(true);
    
    IMAGE_Init(IMAGE_INFO_ADDRESS, 4);
        
    CONFIG_init();    
    CONFIG_setDefault(&xConfig);

    pBC = BC_getInstance(SYS_getModelID());
    CONFIG_load(&xConfig);

    if (pBC != NULL)
    {
        SVRIF_start();
        CNTIF_start(pBC);
    }
    
    SHELL_start(pBC);

    /* Start scheduler */
    osKernelStart();

    /* We should never get here as control is now taken by the scheduler */

    /* Infinite loop */
    while (1)
    {
    }
}

